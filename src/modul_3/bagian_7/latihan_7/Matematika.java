class Matematika {
    // Method untuk 2 parameter int
    int tambah(int a, int b) {
        return a + b;
    }

    // Method untuk 3 parameter int
    int tambah(int a, int b, int c) {
        return a + b + c;
    }

    // Method untuk 2 parameter double
    double tambah(double a, double b) {
        return a + b;
    }
}