package modul_2.bagian_4;

public class Mahasiswa {
    // Atribut Private
    private String nama;
    private String nim;

    // Setter untuk Nama
    public void setNama(String nama) {
        this.nama = nama;
    }

    // Getter untuk Nama
    public String getNama() {
        return nama;
    }

    // Setter untuk NIM
    public void setNim(String nim) {
        this.nim = nim;
    }

    // Getter untuk NIM
    public String getNim() {
        return nim;
    }
}